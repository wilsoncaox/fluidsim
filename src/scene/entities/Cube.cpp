
#include "Cube.hpp"

Cube::Cube() {
  load_mesh();
}

void Cube::load_mesh() {
  vertices = {
    // Edge 0: 0 -> 1 (back bottom edge)
    {{-0.5f, -0.5f, -0.5f}, {1,0,0}, {1,0,0}, {0,0}},
    {{ 0.5f, -0.5f, -0.5f}, {1,0,0}, {1,0,0}, {1,0}},

    // Edge 1: 1 -> 2 (back right edge)
    {{ 0.5f, -0.5f, -0.5f}, {0,1,0}, {0,1,0}, {0,0}},
    {{ 0.5f,  0.5f, -0.5f}, {0,1,0}, {0,1,0}, {1,0}},

    // Edge 2: 2 -> 3 (back top edge)
    {{ 0.5f,  0.5f, -0.5f}, {0,0,1}, {-1,0,0}, {0,0}},
    {{-0.5f,  0.5f, -0.5f}, {0,0,1}, {-1,0,0}, {1,0}},

    // Edge 3: 3 -> 0 (back left edge)
    {{-0.5f,  0.5f, -0.5f}, {1,1,0}, {0,-1,0}, {0,0}},
    {{-0.5f, -0.5f, -0.5f}, {1,1,0}, {0,-1,0}, {1,0}},

    // Edge 4: 4 -> 5 (front bottom edge)
    {{-0.5f, -0.5f,  0.5f}, {1,0,1}, {1,0,0}, {0,0}},
    {{ 0.5f, -0.5f,  0.5f}, {1,0,1}, {1,0,0}, {1,0}},

    // Edge 5: 5 -> 6 (front right edge)
    {{ 0.5f, -0.5f,  0.5f}, {0,1,1}, {0,1,0}, {0,0}},
    {{ 0.5f,  0.5f,  0.5f}, {0,1,1}, {0,1,0}, {1,0}},

    // Edge 6: 6 -> 7 (front top edge)
    {{ 0.5f,  0.5f,  0.5f}, {1,1,1}, {-1,0,0}, {0,0}},
    {{-0.5f,  0.5f,  0.5f}, {1,1,1}, {-1,0,0}, {1,0}},

    // Edge 7: 7 -> 4 (front left edge)
    {{-0.5f,  0.5f,  0.5f}, {0.5f,0.5f,0.5f}, {0,-1,0}, {0,0}},
    {{-0.5f, -0.5f,  0.5f}, {0.5f,0.5f,0.5f}, {0,-1,0}, {1,0}},

    // Edge 8: 0 -> 4 (left bottom edge)
    {{-0.5f, -0.5f, -0.5f}, {1,0,0}, {0,0,1}, {0,0}},
    {{-0.5f, -0.5f,  0.5f}, {1,0,0}, {0,0,1}, {1,0}},

    // Edge 9: 1 -> 5 (right bottom edge)
    {{ 0.5f, -0.5f, -0.5f}, {0,1,0}, {0,0,1}, {0,0}},
    {{ 0.5f, -0.5f,  0.5f}, {0,1,0}, {0,0,1}, {1,0}},

    // Edge 10: 2 -> 6 (right top edge)
    {{ 0.5f,  0.5f, -0.5f}, {0,0,1}, {0,0,1}, {0,0}},
    {{ 0.5f,  0.5f,  0.5f}, {0,0,1}, {0,0,1}, {1,0}},

    // Edge 11: 3 -> 7 (left top edge)
    {{-0.5f,  0.5f, -0.5f}, {1,1,0}, {0,0,1}, {0,0}},
    {{-0.5f,  0.5f,  0.5f}, {1,1,0}, {0,0,1}, {1,0}},
  };

  indices.resize(24);
  for (uint32_t i = 0; i < 24; ++i) {
    indices[i] = i;
  }
}
